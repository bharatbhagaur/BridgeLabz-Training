public class AveragePercentage {
    public static void main(String[] args) {
        int maths=85;//marks in maths
        int physics=95;//marks in physics
        int chemistry=90;//marks in chemistry
        int t=maths+physics+chemistry;//total marks
        int avg=t/3;//average marks
        System.out.println("Sam's average marks in PCM is :"+avg);//output total marks
    }
}